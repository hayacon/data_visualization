function UKElectricity() {

    // Name for the visualisation to appear in the menu bar.
    this.name = 'UK electricity generated by fuel type';

    // Each visualisation must have a unique ID with no special
    // characters.
    this.id = 'UK-electricity';

    // Property to represent whether data has been loaded.
    this._loaded = false;


    // Preload the data. This function is called automatically by the
    // gallery when a visualisation is added.
    this.preload = function () {
        var self = this;
        this._data = loadTable(
            './data/UK-gov/Electricity-generated-by-fuel-type.csv', 'csv', 'header',
            // Callback function to set the value
            // this.loaded to true.
            function (table) {
                self.loaded = true;
            });
    };

    this._waffles = [];

    this.setup = function () {
    
        if (!this.loaded) {
            console.log('Data not yet loaded');
            return;
        }

        this._years = ["2000", "2002", "2004", "2006", "2008", "2010", "2012", "2014", "2016", "2018"];

        this._fueltype = ["Coal", "Oil and other fuel", "Gas", "Nuclear", "Hydro", "Winds & solar", "Other renewables"];

        for (var i = 0; i < this._years.length; i++) {

            // draw a waffle chart in two rows 
            if (i < 5) {
                this._waffles.push(new Waffle(20 + (i * 200), 100, 150, 150, 10, 10, this._data, this._years[i], this._fueltype));

            } else {
                this._waffles.push(new Waffle(20 + (i - 5) * 200, 350, 150, 150, 10, 10, this._data, this._years[i], this._fueltype));
            }
        };
    };

    this.draw = function () {

        // draw each waffle chart
        for (var i = 0; i < this._waffles.length; i++) {
            this._waffles[i].draw();   
        };
        
        // show a fuel type when mouse is on a certain part of waffle chart
        for (var i = 0; i < this._waffles.length; i++) {
            this._waffles[i].checkMouse(mouseX, mouseY);
        };

    }

    this.destroy = function () {};

}
